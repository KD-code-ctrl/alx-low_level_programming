#include "3-calc.h"

/**
 * op_add - is a function that adds two numbers together
 * @a: is a number
 * @b: is a number
 * Return: sum of two number
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - is a function that subtracts two numbers
 * @a: is the first number
 * @b: is the second number
 * Return: the subtracted total
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - is a function that multiplys two numbers together
 * @a: is an integer number
 * @b: is an integer number
 * Return: the product of the two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -  is function that divides two number
 * @a: is a number
 * @b: is a number
 * Return: a value after division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - is a function that finds a remainder
 * @a: is a number
 * @b: is a number
 * Return: the remainder value after operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}

